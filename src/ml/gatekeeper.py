# SECTION 3 — XGBOOST GATEKEEPER
# ─────────────────────────────────────────────────────────────────────────────

class XGBoostGatekeeper:
    """
    Trained on presubmit + postsubmit historical data.
    Predicts Probability of Failure (Pf) for each (module, test) pair.
    Decision: run test if Pf > PF_THRESHOLD, prune otherwise.
    """

    def __init__(
        self,
        presubmit_path:  str = PRESUBMIT_CSV,
        postsubmit_path: str = POSTSUBMIT_CSV,
        pf_threshold:    float = PF_THRESHOLD,
    ):
        self.pf_threshold     = pf_threshold
        self.model            = None
        self.scaler           = StandardScaler()
        self.feature_columns  = []
        self.scale_pos_weight = 1.0
        self._train(presubmit_path, postsubmit_path)

    def _train(self, presubmit_path: str, postsubmit_path: str):
        """Train XGBoost on merged pre+post submit historical data."""
        try:
            df = load_and_merge_datasets(presubmit_path, postsubmit_path)
            X, y_failure, feat_cols = prepare_features(df)
            self.feature_columns = feat_cols

            # Compute class balance for scale_pos_weight
            n_pass   = (y_failure == 0).sum()
            n_fail   = (y_failure == 1).sum()
            if n_fail > 0:
                self.scale_pos_weight = n_pass / n_fail
                log.info("Class balance — Pass: %d  Fail: %d  scale_pos_weight: %.2f",
                         n_pass, n_fail, self.scale_pos_weight)

            # Train/val split (chronological order matters for CI data)
            X_train, X_val, y_train, y_val = train_test_split(
                X, y_failure, test_size=0.15, shuffle=False
            )

            # Scale features
            X_train_s = self.scaler.fit_transform(X_train)
            X_val_s   = self.scaler.transform(X_val)

            # Build model with dynamic scale_pos_weight
            params = {**XGBOOST_PARAMS, "scale_pos_weight": self.scale_pos_weight}
            self.model = xgb.XGBClassifier(**params)

            self.model.fit(
                X_train_s, y_train,
                eval_set=[(X_val_s, y_val)],
                verbose=False,
            )

            # Evaluate
            y_pred_proba = self.model.predict_proba(X_val_s)[:, 1]
            y_pred       = (y_pred_proba > self.pf_threshold).astype(int)

            auc = roc_auc_score(y_val, y_pred_proba)
            log.info("Gatekeeper trained — Val AUC: %.4f", auc)
            log.info("\n%s", classification_report(y_val, y_pred,
                                                   target_names=["pass", "fail"]))

            # Save model
            model_path = OUTPUT_DIR / "gatekeeper_model.json"
            self.model.save_model(str(model_path))
            log.info("Model saved -> %s", model_path)

        except Exception as exc:
            log.warning("Could not train on real data: %s — using mock model", exc)
            self._train_mock()

    def _train_mock(self):
        """Minimal mock training for testing without real data."""
        X_mock = np.array([
            [0.9, 10, 0.95, 0, 0, 0, 0, 0, 1],
            [0.2, 50, 0.10, 1, 0, 1, 0, 0, 2],
            [0.7, 20, 0.80, 0, 0, 0, 1, 0, 1],
            [0.1, 80, 0.05, 0, 1, 1, 0, 0, 3],
            [0.5, 30, 0.60, 1, 1, 0, 0, 1, 2],
            [0.8, 15, 0.90, 0, 0, 0, 0, 0, 1],
            [0.3, 60, 0.20, 1, 0, 1, 1, 0, 2],
        ])
        y_mock = np.array([0, 1, 0, 1, 1, 0, 1])
        self.feature_columns = [
            "cosine_similarity", "change_size", "module_impact_score",
            "is_kafka_consumer", "is_kafka_producer", "is_shared_db",
            "is_frontend_contract", "is_shared_utility", "transitive_depth",
        ]
        X_scaled = self.scaler.fit_transform(X_mock)
        self.model = xgb.XGBClassifier(**{**XGBOOST_PARAMS, "n_estimators": 50})
        self.model.fit(X_scaled, y_mock)
        log.info("Mock gatekeeper trained (replace with real data)")

    def load_pretrained(self, model_path: str):
        """Load a previously saved model instead of retraining."""
        self.model = xgb.XGBClassifier()
        self.model.load_model(model_path)
        log.info("Loaded pretrained model from %s", model_path)

    def predict_failure_prob(self, feature_vector: dict) -> float:
        """
        Predict Probability of Failure for a single (module, test) pair.

        Args:
            feature_vector: dict with keys matching self.feature_columns
                {
                  "cosine_similarity": 0.82,
                  "change_size": 45,
                  "module_impact_score": 0.9,
                  "is_kafka_consumer": 0,
                  "is_kafka_producer": 1,
                  "is_shared_db": 1,
                  "is_frontend_contract": 0,
                  "is_shared_utility": 0,
                  "transitive_depth": 2,
                  "test_flakiness_score": 0.1,
                  ... all other features
                }

        Returns:
            float: Pf (0.0 = definitely passes, 1.0 = definitely fails)
        """
        row = np.array([[feature_vector.get(col, 0.0)
                         for col in self.feature_columns]])
        row_scaled = self.scaler.transform(row)
        pf = float(self.model.predict_proba(row_scaled)[0][1])
        return pf

    def predict_batch(self, feature_df: pd.DataFrame) -> np.ndarray:
        """
        Batch predict Pf for a DataFrame of feature rows.
        Returns array of Pf values.
        """
        cols  = [c for c in self.feature_columns if c in feature_df.columns]
        X     = feature_df[cols].fillna(0).values
        X_s   = self.scaler.transform(X)
        return self.model.predict_proba(X_s)[:, 1]

    def get_feature_importance(self) -> pd.DataFrame:
        """Return feature importance for interpretability."""
        importance = self.model.feature_importances_
        return (
            pd.DataFrame({
                "feature":    self.feature_columns,
                "importance": importance,
            })
            .sort_values("importance", ascending=False)
            .reset_index(drop=True)
        )


